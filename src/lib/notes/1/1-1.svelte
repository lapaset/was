<p>
	In each section of WAS Body of Knowledge there are study topics that must be
	mastered to pass the exam. For this section there is quite a heavy load of
	specifications to understand and remember:
</p>

<h3>Study topics related to specifications and techniques:</h3>
<ul>
	<li>Understand and interpret WCAG 2.1</li>
	<ul>
		<li>
			Understand the relationship between principles, guidelines, and success
			criteria.
		</li>
		<li>
			Understand the intent, requirement, and impact of each principle,
			guideline, and success criterion.
		</li>
		<li>
			Be familiar with sufficient, failure, and advisory techniques for each
			success criterion.
		</li>
		<li>Understand the conformance level designations (A, AA, AAA).</li>
		<li>Identify the conformance level of each WCAG 2.1 success criterion.</li>
		<li>
			Understand the three types of techniques and the W3C vetting process for
			techniques.
		</li>
	</ul>
	<li>Understand and interpret WAI-ARIA 1.1</li>
	<ul>
		<li>Understand the purpose and impact of WAI-ARIA 1.1.</li>
		<li>Understand the WAI-ARIA 1.1 model of names, roles, and values.</li>
		<li>
			Know when and why to use WAI-ARIA 1.1, and when to use standard HTML
			instead.
		</li>
		<li>
			Be familiar with the authoring practices for custom widgets, including
			semantic structure, keyboard behavior, etc
		</li>
	</ul>
	<li>Understand and interpret ATAG 2</li>
	<ul>
		<li>Understand how ATAG 2 applies to web content authoring tools</li>
		<li>
			Understand the meaning and intent of the two main sections of ATAG 2.
		</li>
		<li>
			Understand the intent, requirement, and impact of each principle,
			guideline, and success criterion.
		</li>
		<li>
			Distinguish between good automated practices in authoring tools and good
			practices that require author/user input
		</li>
		<li>
			Understand the power and limitations of automated accessibility authoring
			features
		</li>
	</ul>
</ul>

<h3>Break it down!</h3>

<p>
	The amount of information is a bit terrifying to be honest. So let's break it
	down and start with the first goal: understand the relationship between
	principles, guidelines, and success criteria.
</p>

